// Задача 1 Количество элементов массива на отрезке

// Задайте одномерный массив из 10 целых чисел от 1 до 100. Найдите количество элементов массива, значения которых лежат в отрезке [10,90].

// Начальные условия:

// int[] array = {1, 5, 10, 20, 30, 40, 99, 4, 90, 3}; // Создание массива
// Выводится: 5

using System;
using System.Linq;

class UserInputToCompileForTest
{ 
    // Подсчет количества элементов массива, попадающих в заданный диапазон
    // numbers - массив, в котором ведется подсчет
    // minRange - минимальная граница диапазона
    // maxRange - максимальная граница диапазона
    public static int CountItemsRange(int[] numbers, int minRange, int maxRange)
    {
        int count = 0; // Инициализация счетчика элементов в диапазоне
        foreach (int num in numbers) // Проход по каждому элементу массива numbers
        {
            if (num >= minRange && num <= maxRange) // Проверка, попадает ли текущий элемент в заданный диапазон
            {
                count++; // Увеличение счетчика элементов в диапазоне
            }
        }
        return count; // Возвращаем количество элементов в диапазоне
    }
    
    public static void PrintResult(int[] numbers)
    {
        int minRange = 10; // Задаем минимальную границу диапазона
        int maxRange = 90; // Задаем максимальную границу диапазона
        
        int countInRange = CountItemsRange(numbers, minRange, maxRange); // Вызываем функцию подсчета элементов в заданном диапазоне
        Console.WriteLine(countInRange); // Выводим результат подсчета
    }
}

class Answer
{
    public static void Main(string[] args)
    {
        int[] array;

        if (args.Length >= 1) {
            // Объединяем все аргументы командной строки в одну строку
            string joinedArgs = string.Join(" ", args);

            // Разделяем строку по запятой с пробелом и преобразуем в массив целых чисел
            array = joinedArgs.Split(", ")
                              .Select(int.Parse)
                              .ToArray();
        } else {
            // Если аргументов на входе нет
            array = new int[] {1, 5, 10, 20, 30, 40, 99, 4, 90, 3}; // Создание массива
        }
        
        UserInputToCompileForTest.PrintResult(array); // Вызываем функцию для вывода результата
    }
}


// 1. CountItemsRange: Этот метод подсчитывает количество элементов массива numbers, 
// которые попадают в заданный диапазон от minRange до maxRange. Он проходит по каждому элементу массива 
// и увеличивает счетчик count каждый раз, когда находит элемент в заданном диапазоне.

// 2. PrintResult: Этот метод вызывает CountItemsRange с массивом numbers и заданным диапазоном от 10 до 90. 
// Затем он выводит результат подсчета в консоль.

// 3. Main: В методе Main проверяется наличие аргументов командной строки. 
// Если они есть, то создается массив array из этих аргументов. Если аргументов нет, 
// то создается массив array со значениями по умолчанию. 
// Затем вызывается метод PrintResult для вывода результата подсчета элементов в заданном диапазоне.