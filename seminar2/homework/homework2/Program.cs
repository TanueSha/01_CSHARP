// Задача 2: Напишите программу, которая принимает на вход координаты точки (X и Y), 
// причём X ≠ 0 и Y ≠ 0 и выдаёт номер координатной четверти плоскости, в которой находится эта точка.

Привет! Конечно, вот пример программы на C# с комментариями, которая определяет номер координатной четверти для точки с заданными координатами \(X \neq 0\) и \(Y \neq 0\):

```csharp
using System;

class Program
{
    static void Main()
    {
        // Запрашиваем у пользователя ввод координат точки
        Console.Write("Введите координату X (X ≠ 0): ");
        double x = Convert.ToDouble(Console.ReadLine());

        Console.Write("Введите координату Y (Y ≠ 0): ");
        double y = Convert.ToDouble(Console.ReadLine());

        // Проверяем, что координаты не равны нулю
        if (x == 0 || y == 0)
        {
            Console.WriteLine("Координаты X и Y не должны быть равны нулю.");
        }
        else
        {
            // Определяем, в какой четверти находится точка
            int quadrant = GetQuadrant(x, y);
            Console.WriteLine($"Точка с координатами ({x}, {y}) находится в {quadrant}-й четверти.");
        }
    }

    // Метод для определения четверти
    static int GetQuadrant(double x, double y)
    {
        if (x > 0 && y > 0)
        {
            return 1; // Первая четверть
        }
        else if (x < 0 && y > 0)
        {
            return 2; // Вторая четверть
        }
        else if (x < 0 && y < 0)
        {
            return 3; // Третья четверть
        }
        else // (x > 0 && y < 0)
        {
            return 4; // Четвертая четверть
        }
    }
}
```

Этот код выполняет следующие шаги:
1. Запрашивает у пользователя ввод координат точки \(X\) и \(Y\).
2. Проверяет, что \(X\) и \(Y\) не равны нулю.
3. Определяет номер четверти, в которой находится точка, с помощью метода `GetQuadrant`.
4. Выводит результат на экран.